
public class LinkedList {

	private String data;
	private LinkedList next;
	
	public String getData() {
		return this.data;
	}
	
	public void setData(String data) {
		this.data = data;
	}

	public void setNext(LinkedList next) {
		this.next = next;
	}

	public LinkedList getNext() {
		return this.next;
	}

}
